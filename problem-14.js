/**
 * সমস্যা ১৪: লাইব্রেরির জরিমানা গণনা
 * প্রস্তাবিত ফাংশনের নাম: calculateLibraryFine()
 *
 * সমস্যা: একটি লাইব্রেরি দেরিতে বই ফেরত দেওয়ার জন্য জরিমানা নেয়। নিয়ম: প্রথম ১০ দিনের জন্য কোনো জরিমানা নেই। ১১-৩০ দিনের জন্য, প্রতিদিন ৫ টাকা জরিমানা। ৩০ দিনের বেশি হলে, প্রতিদিন ১০ টাকা জরিমানা। মোট জরিমানা গণনা করার জন্য একটি ফাংশন লিখুন।
 * চ্যালেঞ্জ: যদি ইনপুট ঋণাত্মক হয়, তবে 0 রিটার্ন করুন।
 *
 * নমুনা ইনপুট -> নমুনা আউটপুট
 * 9            -> 0
 * 15           -> 25   (প্রথম ১০ দিন বাদ দিয়ে ৫ দিন * ৫ টাকা)
 * 30           -> 100  (প্রথম ১০ দিন বাদ দিয়ে ২০ দিন * ৫ টাকা)
 * 35           -> 150  (১১-৩০ দিনের জন্য ১০০ + বাকি ৫ দিনের জন্য ৫০)
 * -5           -> 0
 */

function calculateLibraryFine(days) {
    if (days < 0) {
        return 0;
    }

    if (days <= 10) {
        return 0;
    }else if (days <= 30) {
        return (days - 10) * 5;
    }else {
        const first20Days = (30 - 10) * 5;
        const fine30Days = (days - 30) * 10;
        return first20Days + fine30Days; 
    }
}
const number = 35;
const result = calculateLibraryFine(number);
console.log(result);